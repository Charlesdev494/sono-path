export function findRegion(slug: string) {
  return ATLAS.find((r) => r.slug === slug);
}
export function findStructure(regionSlug: string, structureSlug: string) {
  const r = findRegion(regionSlug);
  return r?.estruturas.find((e) => e.slug === structureSlug);
}
